    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Tolong isi semua data terlebih dahulu.", "Tidak ada data produk", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                String categoryName = jTextField1.getText();
                
                controller.addCategory(categoryName);
                
                reset();
                loadData();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Mohon maaf input kategori gagal silahkan coba lagi.", "Input Kategori Gagal", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (kategoriIdSelected != 0) {
            String categoryName = jTextField1.getText();
            
            controller.updateCategory(kategoriIdSelected, categoryName);
            
            loadData();
            reset();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

