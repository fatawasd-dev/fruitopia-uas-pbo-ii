    public void reset() {
        jTextField1.setText("");
    }
    
    public void displayProductList(List<Kategori> kategoriList) {
        String[] columnNames = {"ID", "Nama"};
        Object[][] data = new Object[kategoriList.size()][6];
        for (int i = 0; i < kategoriList.size(); i++) {
            Kategori kategori = kategoriList.get(i);
            data[i][0] = kategori.getId();
            data[i][1] = kategori.getNama();
        }

        jTable1.setModel(new javax.swing.table.DefaultTableModel(data, columnNames));
    }
    
    final public void loadData() {
        List<Kategori> kategori = controller.loadCategoryList();
        displayProductList(kategori);
    }
    

